/* Simplified print function for illustration */
void binary_tree_print(const binary_tree_t *tree)
{
    if (tree == NULL)
        return;
    printf("(%d)", tree->n);
    if (tree->left || tree->right)
    {
        printf(" L:");
        binary_tree_print(tree->left);
        printf(" R:");
        binary_tree_print(tree->right);
    }
    printf("\n");
}

